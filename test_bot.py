    chat_id = call.message.chat.id
    bot.send_message(chat_id, "–ù–∞—á–∏–Ω–∞–µ–º –≤–∏–∫—Ç–æ—Ä–∏–Ω—É –∑–∞–Ω–æ–≤–æ!")
    
